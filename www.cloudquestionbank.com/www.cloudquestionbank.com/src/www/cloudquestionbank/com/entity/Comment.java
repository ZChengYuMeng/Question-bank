package www.cloudquestionbank.com.entity;

import java.sql.Timestamp;

/**
 * 试题评论表:(编号,用户编号,评论时间,评论内容,点踩表编号,题目编号)
 * @author ( 人 )
 *
 */

public class Comment implements java.io.Serializable {

	// Fields

	private Integer id;
	private Integer guestId;
	private Timestamp date;
	private String content;
	private Integer fabulousGarbageId;
	private Integer subjectIdInt;
	// Constructors

	/** default constructor */
	public Comment() {
	}

	

	// Property accessors

	public Comment(Integer id, Integer guestId, Timestamp date, String content,
			Integer fabulousGarbageId, Integer subjectIdInt) {
		super();
		this.id = id;
		this.guestId = guestId;
		this.date = date;
		this.content = content;
		this.fabulousGarbageId = fabulousGarbageId;
		this.subjectIdInt = subjectIdInt;
	}



	public Integer getId() {
		return this.id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getGuestId() {
		return this.guestId;
	}

	public void setGuestId(Integer guestId) {
		this.guestId = guestId;
	}

	public Timestamp getDate() {
		return this.date;
	}

	public void setDate(Timestamp date) {
		this.date = date;
	}

	public String getContent() {
		return this.content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public Integer getFabulousGarbageId() {
		return this.fabulousGarbageId;
	}

	public void setFabulousGarbageId(Integer fabulousGarbageId) {
		this.fabulousGarbageId = fabulousGarbageId;
	}

	public Integer getSubjectIdInt() {
		return subjectIdInt;
	}
	public void setSubjectIdInt(Integer subjectIdInt) {
		this.subjectIdInt = subjectIdInt;
	}
	
}