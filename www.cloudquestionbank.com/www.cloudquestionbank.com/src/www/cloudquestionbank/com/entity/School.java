package www.cloudquestionbank.com.entity;

/**
 * 学校表:(编号,学校名称,地址,状态,邮政编码,校区图标)
 * @author ( 人 )
 *
 */

public class School implements java.io.Serializable {

	// Fields

	private Integer id;
	private String name;
	private String address;
	private Integer state;
	private Integer postalCode;
	private	String  icon;
	// Constructors

	/** default constructor */
	public School() {
	}

	

	// Property accessors

	public School(Integer id, String name, String address, Integer state,
			Integer postalCode, String icon) {
		super();
		this.id = id;
		this.name = name;
		this.address = address;
		this.state = state;
		this.postalCode = postalCode;
		this.icon = icon;
	}



	@Override
	public String toString() {
		return "School [id=" + id + ", name=" + name + ", address=" + address
				+ ", state=" + state + ", postalCode=" + postalCode + ", icon="
				+ icon + "]";
	}



	public Integer getId() {
		return this.id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAddress() {
		return this.address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public Integer getState() {
		return this.state;
	}

	public void setState(Integer state) {
		this.state = state;
	}

	public Integer getPostalCode() {
		return this.postalCode;
	}

	public void setPostalCode(Integer postalCode) {
		this.postalCode = postalCode;
	}



	public String getIcon() {
		return icon;
	}



	public void setIcon(String icon) {
		this.icon = icon;
	}

}